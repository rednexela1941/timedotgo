function runDateAtTests() {
  for (const pair of dateAtTests) {
    const f = pair[0].Format(RFC1123Z);
    console.assert(f === pair[1], `a=${f} doesn't match\nb=${pair[1]}`);
  }

  const now = Now();
  const cpNow = DateAt(
    now.Year(),
    now.Month(),
    now.Day(),
    now.Hour(),
    now.Minute(),
    now.Second(),
    0,
    Local
  );

  const nowF = now.Format(RFC1123Z);
  const cpNowF = cpNow.Format(RFC1123Z);

  console.assert(nowF === cpNowF, `${nowF} != ${cpNowF}`);
}

function runFormatTests() {
  const t = UnixMilli(1233810057012).In("America/Los_Angeles");
  const zName = "PST";
  const zOffset = -28800;
  const { name, offset } = t.Zone();
  console.assert(name === zName, "BAD ZONE INFO");
  console.assert(offset === zOffset, "BAD ZONE INFO");
  for (const item of formatTests) {
    const [name, layout, target] = item;
    // console.log(name, layout, target);
    const val = t.Format(layout);
    console.assert(
      val === target,
      `${name} Failed: target="${target}" != value="${val}" (layout ${layout})`
    );
  }
}

function runParseTests() {
  const formats = [
    "Jan",
    "January",
    "Jan 2006",
    "Jan _2 '06",
    "03:04 PM",
    "03:04 pm",
    "January _2 03:04 PM MST",
    DateOnly,
    TimeOnly,

    DateTime,

    ANSIC,
    UnixDate,
    RubyDate,
    RFC822,
    RFC822Z,
    RFC850,
    RFC1123,
    RFC1123Z,
    RFC3339,
    RFC3339Nano,
    Kitchen,
    Stamp,
    StampMilli,
    StampMicro,
    StampNano,
  ];

  const now = Now();
  for (const f of formats) {
    const format = f;
    const target = now.Format(format);
    const parseT = Parse(format, target);
    const parse = parseT.Format(format);
    const test = parse === target;
    console.assert(
      test,
      `\t\ntarget=${target}\t\nparsed=${parse}\t\nlayout=${format}`
    );
    if (!test) break;
  }
}
